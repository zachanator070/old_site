					 <h3>31 Oct 2014</h3>
					 <p>
						So, something happened this last week that was rather different than my usual schedule of things. Up to this point in class
						we have been talking about character traits, making decisions, and how to become a better person. But Monday... Monday was different. Coming into class on a 
						Monday, I wasn't expecting the class discussion to turn to marital intimacy and eventually sex. Yup that big thing that
						makes everyone at BYU speechless. Our professor told us how no one likes to teach marriage and family any more because of this topic. How do you address this?
						Why are people afraid of this? These two things were painfully obvious as Brother Goodman would teach something and ask for questions afterwards only to hear the
						eerie silence of a room filled with 30 young adults. 
					 </p>
					 
					 <p>
						Sex. Sex. Sex. No one here likes to say it, talk about it, and it seems that they don't even think about it. I feel that there is some social abnormality here where 
						it just doesn't come up in casual conversation. I feel that there is a danger that everyone falls in here where everyone assumes that everyone has the same mindset. 
						This disturbs me. The notion to go about and never question what everyone around you is thinking because everyone assumes it is the same thing for the 
						same reason. But why? I thought this was so strange I decided to talk to other people about it, people outside of this little niche community of Mormons outside 
						of Utah. Sex. What do you believe about it and why? 
					</p>
					
					<p>
						While talking to people, I specifically wanted to know about the topic of abstinence. During class we were told all these reasons and quotations from prophets, professionals, and studies that have been done as well. All of these
						things which were founded upon the principle of abstaining from sex until marriage. I figured if everything was based upon this sole principle, I should understand for myself some
						basic questions. What do I believe about it and why? I feel like if I defined this for myself then the other topics from class would fall in line and I would have a platform to 
						root my understanding in. 
					 </p>
					 
					 <p>
						Trying to define this platform, I wanted to get some opinions on the subject from other people. From people that I have great respect for and I like to think
						that they have been influential on my life in one way or another. The beautiful thing is that their opinions were different and that they all found peace and happiness from
						what they believed. They all had confidence in what they said and had solid reasoning on why they believed what they did. 
					 </p>
					 
					 <p>
						The first person I talked with was Laith Alkarmi. Laith and I have known each other for a long time all the way back from the 7th grade at least. Our common
						interests and his sense of respect has kept our friendship afloat along with the late nights of online conquest. He identified himself 
						as an atheist who does not believe in abstinence. He says that " I do not speak for all or any atheists, but I personally believe that sex is an act that requires great deliberation 
						before committing in a premarital relationship. "Are we ready for it?" is an incredibly complex question with repercussions 
						and benefits that may not be directly evident. It is not like flipping a coin, where a sexual act can bring bad or good results. 
						Uncertainty is an emotion that precedes sex and occurs afterward between non-abstaining individuals. 
						Conquering that uncertainty, resolving the grand questions "am I ready for it? are we ready for it?" defines who we are. 
						Answering those questions in one sexual act creates a feeling of resolution and identity in an individual, we become more sure of who we are and what our beliefs are."
						The reason he states that this is important is because "although these beliefs and identity may be negative, 
						it is part of human life, a necessity, to explore the unknown, including ourselves."
					 </p>
					 
					 <p>
						I then asked him what influences he had in his earlier years that led him to that conclusion. He didn't really express any strong conviction that there was an 
						external source to his thinking from his earlier life, but he did share with me that people of real religious zeal had put him off from it as a source of inspiration.
						He doesn't by any means hate religion but because it was someone who was trying to push their beliefs onto him he was set aback by it. 
						In fact he states his understanding of a religious foundation on the concept of abstinence by saying that "this belief is completely 
						acceptable because it is the individual's right to choose how they conduct themselves, and these religious individuals find great happiness in their relationship with God."
						Honestly I loved what Laith said because it was something different that I have not heard for a long time, but I thought this statement about how other people had treated him
						had impacted me most out of anything that he said.
					 </p>
					 
					 <p>
						Having been a missionary for the LDS church I understand this point exactly. People often think that the missionaries are there to prove others wrong, or to simply convince
						others about Mormon theology, but the missionary purpose is to "invite" others to come unto Christ. Not by dragging them or forcing their nose into a book. I think 
						if everyone had a better respectful mindset about sharing their beliefs we would avoid so much offence and especially situations that Laith had found him self in. 
						Knowing that Laith has this exact attitude towards other people I find it remarkable that we have different views on many things, yet because of a great sense of
						respect towards the others beliefs it has never caused strife. This thought slipped out of my mind while talking to him but he simply said it was our morals
						which was our common ground.
					 </p>
					 
					 <p>
						The next person that fell victim to my questioning was James "Jimmy" Juno. Jimmy is the smartest guy I know that I can call by his first name. He received his 
						undergraduate degree from Rice University and is studying Physics in the graduate program from the University of Maryland. Jimmy was also the salutatorian for our
						high school graduating class, but I think he was better known for playing the flute for our high school marching band. When asked about his beliefs
						about religion he identified most with an agnostic point of view, but if he thought there was a religious belief that was true he would think it was the Catholic
						theology which may have some bias since he was raised in the religion.
					 </p>
					 
					 <p>
						I received an email from Jimmy about the topic of abstinence and what he thought about it. He says "I find the idea of sex in a casual manner mildly repulsive. 
						Mostly due to the fact that no one can deny how sex 
						affects the mind (the chemical cocktail released at orgasm is not something I would ever treat lightly; experiencing that high of serotonin levels with someone 
						who is not committed to you is not something I would choose to do), and the increased risk of sexually transmitted diseases if one has a lot of sexual partners. 
						I also don’t understand the appeal of having a lot of sex partners. Sex is not easy. It can be I suppose if one partner does not care about the other. 
						But a simple gyration until climax is almost certainly not as satisfying as two partners who know each other very well. And if you’re going to have sex for fun, 
						it seems intuitive to me to have sex with someone you know very well and develop a sexual relationship over time. The best context to do that in is a committed relationship."
						He then later explains how a committed relationship is not necessarily defined by marriage. Which is why he does not believe in the concept of abstaining from 
						sex before a marriage vow. 
					 </p>
					 
					 <p>
						I let Alyssum read this paragraph before I posted this and she really liked what Jimmy had said about his definition of sex and what it means
						in a relationship. I had to agree as well as I think most people would how sex is not something to be taken lightly and often has consequences with
						it as well. (Note: "chemical cocktail")
					 </p>
					 
					 <p>
						The last person I talked with was Katy Davis. Katy and I go way back. Like WAY back to grade school. She is super smart as well (she got her undergrad in two
						years nbd) and is currently working in PA school to get her masters in Galveston. Something that I believe is more definitive about Katy is that she was
						married to her husband Daniel last year. When I came back from my mission I had seen that this had happened and I had told her congratulations. I instantly 
						saw the joy and love that she had gained from this experience and had learned of something more that had happened. Katy had gained a real interest in Christianity
						and had started to attend church. 
					 </p>
					 
					 <p>
						Fast forward to now, I was really excited that she agreed to make a statement about her beliefs on abstinence especially with the events that had happened in her life.
						When asked about her beliefs on sex she said "I believe that sex is a beautiful, sacred act that is reserved for a man and a woman who have made a lifelong commitment to one another before God. 
						God designed it that way with our best interests at heart. An analogy from my husband: When two people have an intimate relationship, 
						it is like two pieces of tinfoil that get clumped together into a ball. If this occurs in a relationship with no real commitment, a break up will inevitably occur. 
						When it does, the two pieces will have to be carefully separated. This will most likely result in a few tears or rips in the tinfoil. 
						Even if you are able to escape without any pieces missing, you will never be able to smooth out all the wrinkles. You are forever changed. 
						That is why God wants us to reserve that act for someone who will always be there and who will never have to be separated from you."
						I then asked her to define what a committed relationship means to her and she said "To me, a relationship isn't truly committed until marriage. 
						Taking that oath before God is what binds you together forever."
					 </p>
					 
					<p>
						Katy also brought up a very important part about change. In Christianity there is a belief that exists called repentance in which a person is
						able to change the course of their life which is made possible by the sacrifice of Christ (called the atonement). She notes that 
						"Abstinence is a difficult feat and is usually not without some mistakes along the way. Thankfully we have a loving God who is full of grace."
						Because of this loving God we are able to renew ourselves and forget about the mistakes of the past and move forward.
					
					</p>
					
					<p>
						Now, this question that I asked was much bigger than I thought it was going to be as this has taken me far too long to write so I will simply state that
						I agree with some degree to what everyone said and I found that their comments were inspiring to me. To know that other people out there in the world 
						that have given this subject a thought was refreshing to me. I love that the world has different personalities as well as faiths and I think
						that if we all were able to see the commonality in each other we would have less strife and be able to work through differences better. I think this
						was the main point that I came across after reviewing everything that everyone said. To apply this principle in marriage is an interesting thought to me
						because naturally you are attracted to someone with common personal beliefs and one would not think you would run into this type of application.
					</p>
					
					<p>
						But I would question this thought knowing that no one is that closely similar to where they have absolutely no strife with their chosen spouse. Those
						commonalities from what the marriage is based on should be a foundation that is built off of for the rest of the couple's life. This concept is largely 
						what my beliefs are based off of. Unfortunately this post has grown too large and I will have to save that for another time. Until then if you have read this far
						thank you and I wish you all a good week until next time. 
						
					</p>
					
