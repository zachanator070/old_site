					 <h3>18 October 2014</h3>
					 <p>
					 So this week was crazy as usual but I was especially anxious because I had a huge CS test which took me
					 upwards of 4 hours to take...proofs... all I have to say. So with this happening I was kinda on edge and was
					 rather lost at first on what I was going to do this week for my character development class. But lo and behold 
					 Brother Goodman dropped the bomb of all questions on us last week.
					 </p>
					 
					 <p>
					 What is love? This was the question that I sought after this week to define and how to know when someone was in love.
					 It was especially weird this week since Alyssum was gone for a couple of days to her cousins wedding so my life was 
					 pretty much over as the center of my universe was blown up. Let me tell you it was a sad chapter in my life. So in 
					 light of this event I decided I was going to drown my sorrows in an engaging game of Dungeons and Dragons with my
					 mission buddies. 
					 </p>
					 
					 <p>
					 Anyway we won't much further into that depressing hole but as I thought of this question I wanted to get the opinions of what other people
					 thought love was defined around. I asked them specifically why they loved their husbands. Something I thought was a good indicator of 
					 this was when two people had trust in each other. Considering this I asked the participants if they knew of a particular event where
					 they knew that their significant other had trust in them.
					 </p>
					 
					 <img src="pics/momanddad.jpg" height="200" width="300" align="right">
					 
					 <p>
					 Armed with these questions I began to pester those closest to me, starting with my mother. When asked what she noticed when she realized she was in love,
					 she said that it was when she could tell that he was honest about who he was. She liked how he was very straight forward
					about how he felt. His action and feelings matched what he said. She didn't have to play 
					games about how he really felt about mom. It was easy for her to see what his character
					was and because of this realization she fell in love with him.
					 </p>
					 
					 <p>
					 This answer was like it was straight out of brother Goodman's class. It wasn't even a week ago that we had talked about looking for character qualities
					instead of personality traits. This was really cool for me because I could tell that the class must have some true principles behind it if those same ideals
					 worker so well with my parents. When I asked her about when she knew that my dad trusted her I got quite the interesting story from her. She mentioned a time
					 where my dad LET MY MOM SHAVE HIS BEARD. If anything says trust then it must be this.
					 </p>
					 
					 <img src="pics/kaylaandmitch.jpg" height="300" width="205" align="left">
					 
					 <p>
					 After my mom I called my sister Kayla who recently gave birth to her beautiful son Sam. I asked her the questions concerning my quest
					 for understanding and she mentioned how she noticed how her husband, Mitch, had been fiercely loyal to her. She had noticed he treated 
					 his friends really well and the way he treated kayla with the same individual attention. She mentioned that she knew his feelings were true
					 instead of an after effect of him just looking to have a good time.
					 </p>
					 
					 <p>
					 By the time that I was done talking about love Mitch had entered the room with Kayla and he began to explain why he had trust in Kayla. Mitch and Kayla
					had somewhat of a long distance relationship as mitch would attend the police academy and would drive up to Pleasant Grove to see Kayla. Each time Kayla would
					have food for him and would see that she was dedicated even though they had some distance between them. From what I gathered was dedication that their love was
					based off of.
					 </p>
					 
					 <p>
					 Right away I noticed that both my sister and my mother's love was focused around the character qualities of my dad and my brother in law which was
					 once again a huge topic that was talked about in class. Of course now after talking with my family about this question of love I presented it to 
					 Alyssum and asked her what she thought.
					 </p>
					 
					 <p>
					 Alyssum described love not necessarily as an event nor that it has the same meaning towards
					those who you love. But she said that she knew it was based on emotion for sure. She explained that she knew she was in love with during the times she was upset.
					She said I was always considerate and always knew what to say to me to make her feel better. It was
					at then that she knew that I would do anything for her and that I indeed love her. She also mentioned that 
					she had trusted me when I had admitted to my imperfections and weaknesses but acknowledge them and try to do better.
					</p>
					
					<p>
					So bottom line. What is love? From what I have gathered this week I would say that love is centered around what you see in the other person.
					Not just a personality quirk but something that defines what their character is like. I feel that trust is the sticky stuff that keeps it
					held together especially when bad times come knocking on your door. I feel the more active you are to keep maintaining trust in the relationship,
					and continually recognizing (and showing) those character qualities that your love is based on, then love will last much longer than just simply 
					after that newly-wed phase.
					</p>
					
