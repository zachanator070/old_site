					 <h3>10 Oct 2014</h3>
					 <p>
						So after a long weekend I am finally able to write this although I don't think I'll post this till later because the site...
						the colors... I don't have a perfect "artistic eye" so I have to fix a lot of colors and layout things with the site before I get 
						it posted. Hopefully my loving fiance will help me fix all of this haha :)
					</p>
					
					<p>
						This past week was the first week that I'm starting this project for my Marriage and Family Prep class with Dr Goodman.
						And it was rather interesting while trying to apply class principles.
						First I guess I should say that classes at BYU are a little different. In my mind, unless you go to a particular Christian 
						school, I imagine religion to be a touchy subject at other universities if not totally taboo. At BYU things are a little
						different as prayers are given and hymns sung at the start of every class.
					</p>
					
					<p>
						With that in mind God and Mormon doctrine are not shy subjects in a class about marriage. Most of what we read and talk about come
						from the addresses that prophets and apostles have given at the biannual general church meetings. This past week we particularly 
						talked about an address given by Richard G. Scott about the essential attributes of what you look for in your spouse. He gave some 
						specific things that we should be looking for but Dr. Goodman took it one step further and had us organize a list of our own. The
						list that we made was rather short and consisted of character traits NOT personality traits. Then we were to look at the list and find 
						an attribute that WE were to work on.
					</p>
					
					<p>
						So as I looked at mine I looked upon my recent engagement and thought that I should do everything I can to make sure that my relationship
						goes happily and smoothly before Alyssum are to wed in December. The horrific truth that I realized is that I don't have much experience 
						in the marriage field, as most young single people are in this world. I decided that I would assume that I know NOTHING about relationships 
						or what it is like to have one. Then I would take something from class or from church that week and do it.
					</p>
					
					<p>
						After thinking about this I started to pay attention again and Dr Goodman jumped right into the meat of what all of us were there for.
					</p>
					
					<p>Dating.</p>
					
					<p>
						He simply asked why do we do it? He explained how the notion of formally dating a person was getting outdated. Casual meetings between young people
						are getting more and more popular while asking someone out on a "date" is something that mom and dad used to do. The fear of commitment, economic
						situations, lowering of social morals, and the distractions of school and work has made casual social experiences between young single people far more
						appealing than going to an event that is paid for, paired off, and planned.
					</p>
					
					<img src="pics/pic4.jpg" height="300" width="200" align="left">
					
					<p>
						After this lovely explanation of the unpopularity of what I had centred my life around for the past 4 months, the question still remained why date someone?
						Thinking about this I realized another horrible truth... I hadn't been on a date for about 3 weeks. A date being alone and doing something engaging with my significant
						other. Alyssum agreed that yes we HAD to go get ice cream and eat delicious food because Dr. Goodman told us to. So off we went. Cold stone was our destination
						and brownie fudge ice cream was our mission. While feasting upon chocolate and cheesecake, I asked her the question that was posed in class.
					</p>
					
					<p>
						Why date someone? She told me about how most relationships in high school in the Philippines went. You texted someone a lot and told your friends that
						you "liked" them. Eventually said person would find out and they would return or deny your affections. The relationship consisted of kissing and holding hands
						and changing your status on facebook. She explained how it was very superficial and hollow. On the other hand she said that while she has been "dating" me 
						that she has gotten to know who I was rather who I thought I was, or what other people thought of me. She liked how dating gave us a foundation that 
						she was able to base how she felt about me when no one else was around. 
					</p>
					
					<img src="pics/pic3.jpg" height="350" width="200" align="right">
					
					<p>
						After this we agreed that dating was a good idea. I asked if I should date her even after we are married. She said yes of course because dating defines
						a relationship and continuing to date redefines that relationship every time. I asked her if I did a good job and it was concluded that my dating expertise
						could use some redefinition as well. 
					</p>
					
					<p>
						Bottom line, Alyssum told me dating for her helps defines a relationship because it helps her see who that person is. Also that it was important to her that
						we regularly spend time with each other just her and me, one on one. I feel like things are always different when you have that alone time with someone. Not only 
						that but seeing that person in different situations. For me character traits are seen in more than just one situation and dating can reveal those characteristics
						that I should be looking for in a spouse. 
					</p>

					<p>
						I guess I should mention some disclaimers as well. One thing that was brought up in class was the danger of falling head over heels for someone and being "blinded by love."
						A major thing for me is how my friends see me in my relationship as well and it's really cool to see how people respond to Alyssum as well and how amazed they are 
						at her. This sort of feedback is only gained by being around other people which was a really good indicator for me which is why I feel like its important not to
						neglect everyone else in your life just because you are dating someone or in a relationship.
					</p>
						
						
					<p>	
						Anyway that's what happened this past week and hope at least that someone else reads this.
						On another note hey I'm engaged!!! Yay! This last week end we took engagement pictures. Here is a good one... Have a great week, see ya next week.
					 </p>
					 
